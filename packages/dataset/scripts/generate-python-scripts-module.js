import fs from "node:fs"
import path from "node:path"
import { fileURLToPath } from "node:url"

const currentDir = path.dirname(fileURLToPath(import.meta.url))
const sourceDir = path.join(currentDir, "..", "src", "file", "scripts")
const destPath = path.join(currentDir, "..", "src", "file", "scripts.generated.ts")

const scriptEntries = fs
  .readdirSync(sourceDir)
  .filter((file) => file.endsWith(".py"))
  .sort()
  .map((file) => {
    const content = fs.readFileSync(path.join(sourceDir, file))
    return [file, content.toString("base64")]
  })

const generated = `// Generated by packages/dataset/scripts/generate-python-scripts-module.js.
// Do not edit by hand.

export const PYTHON_SCRIPT_BASE64_BY_NAME = Object.freeze(${JSON.stringify(
  Object.fromEntries(scriptEntries),
  null,
  2,
)}) as Readonly<Record<string, string>>
`

const current = fs.existsSync(destPath) ? fs.readFileSync(destPath, "utf8") : ""
if (current !== generated) {
  fs.writeFileSync(destPath, generated)
  console.log(`Generated ${path.relative(process.cwd(), destPath)} with ${scriptEntries.length} script(s)`)
} else {
  console.log(`Generated module already up to date: ${path.relative(process.cwd(), destPath)}`)
}
